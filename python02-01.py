# 주석을 남기기 위한 기호 '#' 이다.
# 파이썬이 지원하는 숫자형
# 정수, 실수, 복소수, 8진수, 16진수
# 8진수 :  0o숫자(영문 소문자 혹은 대문자 o를 사용)
# 16진수 : 0x숫자(영문 소문자 혹은 대문자 x를 사용)

print("정수형 출력")
print(123)
print(-178)

print("실수형 출력")
print(3.14)
print(-3.14)

print("8진수 출력")
print(0o123)
print(-0o123)

print("16진수 출력")
print(0x1234)
print(-0x1234)